def solution(n):
    def is_prime(n):
        if n <= 1:
            return False
        for i in range(2, int(n**0.5) + 1):
            if n % i == 0:
                return False
        return True
    answer = 0
    for num in range(2, n+1):
        if is_prime(num):
            answer += 1
    return answer
